function Thumb({
  url,
  fallback,
  tone,
  kind,
}: {
  url: string | null;
  fallback: string;
  tone: AvatarTone;
  kind: 'event' | 'artist' | 'venue';
}) {
  // Slightly different shape per kind — circle for artists (matches the
  // EventCard avatar cluster), rounded square for events / venues.
  const shape = kind === 'artist' ? 'rounded-full' : 'rounded-lg';
  return (
    <div
      className={cn(
        'flex h-9 w-9 shrink-0 items-center justify-center overflow-hidden border',
        shape,
        !url && AVATAR_BG[tone],
        'font-display text-[11px] font-semibold',
      )}
      aria-hidden
    >
      {url ? (
        // eslint-disable-next-line @next/next/no-img-element
        <img
          src={url}
          alt=""
          loading="lazy"
          className={cn(
            'h-full w-full object-cover',
            kind === 'artist' && 'object-top',
          )}
        />
      ) : (
        initialsFor(fallback)
      )}
    </div>
  );
}
